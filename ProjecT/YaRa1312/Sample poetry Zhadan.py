# Сидить на ліжку напроти й кличе мене до помсти.
# Каже мені: «Малий, в тебе немає ні злості, ні сили.
# Ти їм здав свою злість, мов провідникові постіль.

# В тебе немає, малий, ні спадщини, ні країни,
