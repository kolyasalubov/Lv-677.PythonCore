math1 = int(input("Пожалуйста, введите первое число: "))
math2 = int(input("Пожалуйста, введите второе число: "))
print ("Сложение, результат :", math1 + math2)
print ("Вычитание, результат :", math1 - math2)
print ("Умножение, результат :", math1 * math2)
print ("Деление, результат :", math1 / math2)
print ("В степень, результат :", math1 ** math2)