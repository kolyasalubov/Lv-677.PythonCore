first_number = float(input("Put the first number: "))
second_number = float(input("Put the second number: "))

print(f"{first_number} plus {second_number} equals: ", first_number + second_number)
print(f"{first_number} minus {second_number} equals: ", first_number - second_number)
print(f"{first_number} times {second_number} equals: ", first_number * second_number)
print(f"{first_number} devided by {second_number} equals: ", first_number / second_number)
print(f"{first_number} to the power of {second_number} equals: ", first_number ** second_number)