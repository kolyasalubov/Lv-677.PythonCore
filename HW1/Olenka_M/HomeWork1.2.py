""" тільки для дійсних значень і підмножин, без перехватів помилок неправильного вводу"""

a = float(input('Enter float number a\n'))
b = float(input('Enter float number b\n'))

sum_a_b = a + b
diff_a_b = a - b
product_a_b = a * b
share_a_b = a / b
degree_a_b = a ** b

print('\n')
print('Sum a and b = ', sum_a_b)
print('Difference a and b = ', diff_a_b)
print('Product a and b = ', product_a_b)
print('Share b of a = ', share_a_b)
print('a to the power of b = ', degree_a_b)
